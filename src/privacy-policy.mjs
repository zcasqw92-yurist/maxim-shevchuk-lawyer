export const injectPrivacyPolicy = (html, pathname) => {
  if (pathname !== "/politika-konfidencialnosti") return html;
  const pattern = /<section class="legal-page">[\s\S]*?<\/section>/;
  if (!pattern.test(html)) throw new Error("Не найден исходный блок политики конфиденциальности");
  return html.replace(pattern, policySection());
};
